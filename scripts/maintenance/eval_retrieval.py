"""eval_retrieval — retrieval quality harness (CLAUDE.md-adjacent spec:
docs/superpowers/specs/2026-07-11-retrieval-eval-harness-design.md).

Subcommands:
    mine     regenerate mined (sq-) qrels from the vault's saved queries
    run      score the CURRENT config.yml configuration against the qrels
    compare  diff two run reports (aggregate deltas, config diff, per-query)

Gold labels are file-level and known-incomplete: recall is a lower bound,
so only run-to-run deltas are meaningful, never absolute scores.

Synthetic (syn-) qrels are agent-authored data, regenerated by procedure
(see the spec §1), never by this script. `mine` preserves them verbatim.
"""

from __future__ import annotations

import argparse
import csv
import hashlib
import json
import math
import re
import subprocess
import sys
from datetime import date, datetime, timezone
from pathlib import Path
from typing import Callable

from scripts.wiki_lib import frontmatter
from scripts.wiki_lib.locations import vault_path, work_path

# Metric cutoffs are fixed so every report is comparable; retrieval depth
# (--k on `run`) is the only knob.
RECALL_K = 20
NDCG_K = 10
MRR_K = 10


def dedupe_to_files(results: list[dict]) -> list[str]:
    """Chunk results (rank order) -> unique file_ids at first-hit rank."""
    seen: set[str] = set()
    ordered: list[str] = []
    for r in results:
        fid = r["file_id"]
        if fid not in seen:
            seen.add(fid)
            ordered.append(fid)
    return ordered


def recall_at_k(ranked: list[str], relevant: set[str], k: int) -> float:
    if not relevant:
        return 0.0
    return len(set(ranked[:k]) & relevant) / len(relevant)


def ndcg_at_k(ranked: list[str], relevant: set[str], k: int) -> float:
    dcg = sum(1.0 / math.log2(i + 2) for i, fid in enumerate(ranked[:k]) if fid in relevant)
    ideal = sum(1.0 / math.log2(i + 2) for i in range(min(len(relevant), k)))
    return dcg / ideal if ideal else 0.0


def reciprocal_rank(ranked: list[str], relevant: set[str], k: int) -> float:
    for i, fid in enumerate(ranked[:k]):
        if fid in relevant:
            return 1.0 / (i + 1)
    return 0.0


REQUIRED_KEYS = {"qid", "query", "relevant_file_ids", "source", "split", "created"}
VALID_SOURCES = {"saved_query", "synthetic"}
VALID_SPLITS = {"dev", "holdout"}


def load_qrels(path: Path) -> list[dict]:
    """Parse + validate qrels.jsonl. Fails loudly with path:lineno context."""
    records: list[dict] = []
    seen_qids: set[str] = set()
    for lineno, line in enumerate(path.read_text(encoding="utf-8").splitlines(), start=1):
        if not line.strip():
            continue
        where = f"{path}:{lineno}"
        try:
            rec = json.loads(line)
        except json.JSONDecodeError as e:
            raise ValueError(f"{where}: invalid JSON: {e}") from e
        if not isinstance(rec, dict):
            raise ValueError(f"{where}: expected object, got {type(rec).__name__}")
        missing = REQUIRED_KEYS - rec.keys()
        if missing:
            raise ValueError(f"{where}: missing keys {sorted(missing)}")
        if rec["source"] not in VALID_SOURCES:
            raise ValueError(f"{where}: bad source {rec['source']!r}")
        if rec["split"] not in VALID_SPLITS:
            raise ValueError(f"{where}: bad split {rec['split']!r}")
        if not isinstance(rec["relevant_file_ids"], list):
            raise ValueError(f"{where}: relevant_file_ids must be a list")
        if not rec["relevant_file_ids"]:
            raise ValueError(f"{where}: empty relevant_file_ids")
        if rec["qid"] in seen_qids:
            raise ValueError(f"{where}: duplicate qid {rec['qid']!r}")
        seen_qids.add(rec["qid"])
        records.append(rec)
    return records


def write_qrels(path: Path, records: list[dict]) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text("\n".join(json.dumps(r, ensure_ascii=False) for r in records) + "\n", encoding="utf-8")


_FILE_ID_RE = re.compile(r"^- file_id: `([0-9a-f]{8,16})`", re.M)
_PATH_RE = re.compile(r"^- path: `([^`]+)`", re.M)
_RESULT_HEAD_RE = re.compile(r"^### \d+\.\s+\[(?P<title>.+?)\]\(", re.M)


def _section(body: str, heading: str) -> str:
    """Text of a `## <heading>` section, up to the next `## ` or EOF."""
    m = re.search(rf"^{re.escape(heading)}\s*$(.*?)(?=^## |\Z)", body, re.M | re.S)
    return m.group(1).strip() if m else ""


def parse_saved_query(text: str, stem: str, created: str) -> dict | None:
    """One saved-query .md -> a qrels record, or None if unusable.

    Positives = top-result files referenced in the Answer text (by file_id,
    relpath basename, or case-insensitive title). Answer citation is the
    relevance judgment; raw top-results would just reward the current baseline.
    """
    meta, body = frontmatter.split(text)
    question = str(meta.get("question") or "").strip()
    answer = _section(body, "## Answer")
    top = _section(body, "## Top results")
    if not (question and answer and top):
        return None

    answer_lower = answer.lower()
    cited: list[str] = []
    # Each "### N. [Title](...)" block carries one file_id + path line.
    blocks = re.split(r"(?=^### \d+\.)", top, flags=re.M)
    for block in blocks:
        fid_m = _FILE_ID_RE.search(block)
        if not fid_m:
            continue
        fid = fid_m.group(1)
        title_m = _RESULT_HEAD_RE.search(block)
        path_m = _PATH_RE.search(block)
        title = title_m.group(1) if title_m else ""
        basename = Path(path_m.group(1)).name if path_m else ""
        referenced = fid in answer or (basename and basename in answer) or (title and title.lower() in answer_lower)
        if referenced and fid not in cited:
            cited.append(fid)
    if not cited:
        return None
    return {
        "qid": f"sq-{stem}",
        "query": question,
        "relevant_file_ids": cited,
        "source": "saved_query",
        "split": "dev",
        "created": created,
    }


def cmd_mine(args: argparse.Namespace) -> int:
    """Regenerate sq- records from the vault; preserve syn- records and
    existing split/created assignments (the holdout stays frozen)."""
    vault = vault_path()
    sq_dir = vault / "_index" / "saved_queries"
    if not sq_dir.is_dir():
        print(f"error: saved_queries dir not found: {sq_dir} (set WIKI_VAULT?)", file=sys.stderr)
        return 1

    qrels_path = Path(args.qrels)
    existing = load_qrels(qrels_path) if qrels_path.exists() else []
    prior = {r["qid"]: r for r in existing}
    synthetic = [r for r in existing if r["source"] == "synthetic"]

    today = date.today().isoformat()
    mined: list[dict] = []
    skipped: list[str] = []
    for md in sorted(sq_dir.glob("*.md")):
        rec = parse_saved_query(md.read_text(encoding="utf-8"), stem=md.stem, created=today)
        if rec is None:
            skipped.append(md.name)
            continue
        if rec["qid"] in prior:  # keep frozen split + original created date
            rec["split"] = prior[rec["qid"]]["split"]
            rec["created"] = prior[rec["qid"]]["created"]
        mined.append(rec)

    write_qrels(qrels_path, synthetic + mined)
    print(f"mined {len(mined)} saved queries -> {qrels_path} (kept {len(synthetic)} synthetic)")
    if skipped:
        print(f"skipped {len(skipped)} with no answer-cited positives: {', '.join(skipped)}")
    print("new mined records default to split=dev; move any to holdout by editing the file.")
    return 0


def load_manifest_file_ids(path: Path) -> set[str]:
    with path.open(newline="", encoding="utf-8") as fh:
        return {row["file_id"] for row in csv.DictReader(fh)}


def _score_query(rec: dict, ranked_files: list[str]) -> dict:
    relevant = set(rec["known_positives"])
    rr = reciprocal_rank(ranked_files, relevant, MRR_K)
    # First hit over the FULL ranked list — independent of the rr@10 cutoff,
    # so a positive at rank 11-20 shows its true rank instead of None.
    first_hit = next((i + 1 for i, fid in enumerate(ranked_files) if fid in relevant), None)
    top = ranked_files[:RECALL_K]
    return {
        "qid": rec["qid"],
        "source": rec["source"],
        "split": rec["split"],
        "recall@20": recall_at_k(ranked_files, relevant, RECALL_K),
        "ndcg@10": ndcg_at_k(ranked_files, relevant, NDCG_K),
        "rr@10": rr,
        "first_hit_rank": first_hit,
        "missed_file_ids": [f for f in rec["known_positives"] if f not in set(top)],
        "top_files": ranked_files[:10],
    }


def aggregate(rows: list[dict]) -> dict | None:
    if not rows:
        return None
    n = len(rows)
    return {
        "recall@20": sum(r["recall@20"] for r in rows) / n,
        "ndcg@10": sum(r["ndcg@10"] for r in rows) / n,
        "mrr@10": sum(r["rr@10"] for r in rows) / n,
        "n": n,
    }


def run_eval(
    qrels: list[dict],
    search_fn: Callable[..., list[dict]],
    manifest_ids: set[str],
    *,
    k: int,
    include_holdout: bool,
) -> dict:
    """Score every qrel; returns the report body (caller adds metadata).

    Corpus-drifted qrels (no positive left in the manifest) are excluded and
    reported — never silently dropped. Holdout rows are only scored when
    explicitly requested (the overfitting gate).
    """
    per_query: list[dict] = []
    excluded: list[str] = []
    for rec in qrels:
        known = [f for f in rec["relevant_file_ids"] if f in manifest_ids]
        if not known:
            excluded.append(rec["qid"])
            continue
        if rec["split"] == "holdout" and not include_holdout:
            continue
        results = search_fn(rec["query"], k)
        ranked = dedupe_to_files(results)
        per_query.append(_score_query({**rec, "known_positives": known}, ranked))

    dev_rows = [r for r in per_query if r["split"] == "dev"]
    hold_rows = [r for r in per_query if r["split"] == "holdout"]
    by_source = {
        "dev": {
            src: agg for src in sorted(VALID_SOURCES) if (agg := aggregate([r for r in dev_rows if r["source"] == src]))
        }
    }
    return {
        "aggregate": {"dev": aggregate(dev_rows), "holdout": aggregate(hold_rows)},
        "by_source": by_source,
        "excluded_qids": excluded,
        "per_query": per_query,
    }


def format_summary(report: dict) -> str:
    lines = []
    f = report["flags"]
    n_pq = len(report["per_query"])
    lines.append(f"qrels scored: {n_pq} · {len(report['excluded_qids'])} excluded (file_id gone from manifest)")
    lines.append(
        f"config: {f['mode']}, rerank={'on' if f['rerank'] else 'off'}, "
        f"expand_graph={f['expand_graph']}, k={f['k']} · git {report['git_rev']}"
    )
    lines.append("NOTE: positives are incomplete — scores are lower bounds; compare runs, not absolutes.")
    lines.append("")
    lines.append(f"{'':24}{'recall@20':>10}{'nDCG@10':>10}{'MRR@10':>9}{'n':>5}")

    def row(name: str, agg: dict | None) -> str:
        if agg is None:
            return f"{name:<24}{'—':>10}{'—':>10}{'—':>9}{'—':>5}"
        return f"{name:<24}{agg['recall@20']:>10.3f}{agg['ndcg@10']:>10.3f}{agg['mrr@10']:>9.3f}{agg['n']:>5}"

    lines.append(row("dev (all)", report["aggregate"]["dev"]))
    for src, agg in report["by_source"]["dev"].items():
        lines.append(row(f"  {src}", agg))
    if report["aggregate"]["holdout"] is not None:
        lines.append(row("holdout", report["aggregate"]["holdout"]))
    return "\n".join(lines)


def _git_rev(cwd: Path) -> str:
    try:
        out = subprocess.run(
            ["git", "rev-parse", "--short", "HEAD"],
            cwd=cwd,
            capture_output=True,
            text=True,
            timeout=10,
        )
        return out.stdout.strip() or "unknown"
    except (OSError, subprocess.TimeoutExpired):
        return "unknown"


def cmd_run(args: argparse.Namespace) -> int:
    work = work_path()
    index_dir = work / "01_data" / "index"
    if not (index_dir / "chunks.jsonl").exists() or not (index_dir / "manifest.csv").exists():
        print("error: index not built — run: uv run python -m scripts.cli build", file=sys.stderr)
        return 1
    qrels_path = Path(args.qrels)
    if not qrels_path.exists():
        print(f"error: qrels not found: {qrels_path} — run `eval mine` first", file=sys.stderr)
        return 1

    qrels = load_qrels(qrels_path)
    manifest_ids = load_manifest_file_ids(index_dir / "manifest.csv")

    from scripts.serve import retrieval as wr  # deferred: loads index artifacts
    from scripts.wiki_lib.config import get_config

    def search_fn(query: str, k: int) -> list[dict]:
        return wr.search(
            query,
            k=k,
            mode=args.mode,
            rerank_results=args.rerank,
            expand_graph=args.expand_graph,
        )

    report = run_eval(qrels, search_fn, manifest_ids, k=args.k, include_holdout=args.holdout)

    cfg = get_config().retrieval.model_dump(mode="json")
    ts = datetime.now(timezone.utc).strftime("%Y-%m-%dT%H-%M-%SZ")
    report = {
        "label": args.label,
        "timestamp": ts,
        "git_rev": _git_rev(work),
        "config": cfg,
        "flags": {"mode": args.mode, "rerank": args.rerank, "expand_graph": args.expand_graph, "k": args.k},
        **report,
    }

    runs_dir = work / "01_data" / "eval" / "runs"
    runs_dir.mkdir(parents=True, exist_ok=True)
    out = runs_dir / f"{ts}_{args.label}.json"
    out.write_text(json.dumps(report, indent=2, ensure_ascii=False), encoding="utf-8")

    print(format_summary(report))
    print(f"\nreport -> {out}")

    if args.holdout:  # the peek log: every holdout look is on the record
        cfg_hash = hashlib.sha1(json.dumps(cfg, sort_keys=True).encode()).hexdigest()[:10]
        log_path = work / "00_inputs" / "eval" / "holdout_runs.jsonl"
        entry = {
            "date": ts,
            "label": args.label,
            "git_rev": report["git_rev"],
            "config_hash": cfg_hash,
            "holdout": report["aggregate"]["holdout"],
        }
        with log_path.open("a", encoding="utf-8") as fh:
            fh.write(json.dumps(entry, ensure_ascii=False) + "\n")
        print(f"holdout peek logged -> {log_path}")
    return 0


def flatten_config(cfg: dict, prefix: str = "") -> dict[str, object]:
    """Recursively flatten nested config dict into dotted-key format."""
    flat: dict[str, object] = {}
    for key, val in cfg.items():
        dotted = f"{prefix}{key}"
        if isinstance(val, dict):
            flat.update(flatten_config(val, prefix=f"{dotted}."))
        else:
            flat[dotted] = val
    return flat


def format_compare(a: dict, b: dict) -> str:
    """Format a diff between two run reports for display."""
    lines = [
        f"A = {a['label']} ({a['timestamp']}, git {a['git_rev']})",
        f"B = {b['label']} ({b['timestamp']}, git {b['git_rev']})",
        "",
    ]

    fa, fb = flatten_config(a["config"]), flatten_config(b["config"])
    diff_keys = sorted(k for k in fa.keys() | fb.keys() if fa.get(k) != fb.get(k))
    if diff_keys:
        lines.append("config diff:")
        for k in diff_keys:
            lines.append(f"  {k}: {fa.get(k)!r} -> {fb.get(k)!r}")
    else:
        lines.append("config diff: none (flags may still differ)")
    if a["flags"] != b["flags"]:
        lines.append(f"flags: {a['flags']} -> {b['flags']}")
    lines.append("")

    agg_a, agg_b = a["aggregate"]["dev"], b["aggregate"]["dev"]
    if agg_a is None or agg_b is None:
        lines.append("dev aggregate: unavailable in one or both reports (no scored dev queries)")
    else:
        lines.append(f"{'dev aggregate':<16}{'A':>8}{'B':>8}{'Δ':>8}")
        for metric in ("recall@20", "ndcg@10", "mrr@10"):
            d = agg_b[metric] - agg_a[metric]
            lines.append(f"{metric:<16}{agg_a[metric]:>8.3f}{agg_b[metric]:>8.3f}{d:>+8.3f}")
    lines.append("")

    pa = {r["qid"]: r for r in a["per_query"]}
    pb = {r["qid"]: r for r in b["per_query"]}
    moved = [
        (qid, pb[qid]["ndcg@10"] - pa[qid]["ndcg@10"])
        for qid in sorted(pa.keys() & pb.keys())
        if abs(pb[qid]["ndcg@10"] - pa[qid]["ndcg@10"]) > 1e-9
    ]
    regressions = sorted((m for m in moved if m[1] < 0), key=lambda m: m[1])
    improvements = sorted((m for m in moved if m[1] > 0), key=lambda m: -m[1])
    lines.append(f"per-query nDCG regressions ({len(regressions)}):")
    for qid, d in regressions:
        missed = ", ".join(pb[qid]["missed_file_ids"]) or "none"
        lines.append(f"  {qid}: {d:+.3f}  missed in B: {missed}")
    lines.append(f"per-query nDCG improvements ({len(improvements)}):")
    for qid, d in improvements:
        lines.append(f"  {qid}: {d:+.3f}")
    only = sorted(set(pa) ^ set(pb))
    if only:
        lines.append(f"qids present in only one report (not compared): {', '.join(only)}")
    return "\n".join(lines)


def cmd_compare(args: argparse.Namespace) -> int:
    """Load two run report JSON files and print their diff."""
    for path in (args.report_a, args.report_b):
        if not Path(path).exists():
            print(f"error: report not found: {path}", file=sys.stderr)
            return 1
    a = json.loads(Path(args.report_a).read_text(encoding="utf-8"))
    b = json.loads(Path(args.report_b).read_text(encoding="utf-8"))
    print(format_compare(a, b))
    return 0


_DEFAULT_QRELS = "00_inputs/eval/qrels.jsonl"


def main(argv: list[str] | None = None) -> None:
    """CLI entrypoint: parse mine/run/compare subcommands and dispatch."""
    parser = argparse.ArgumentParser(prog="eval", description="Retrieval quality harness (mine / run / compare).")
    sub = parser.add_subparsers(dest="cmd", required=True)

    p_mine = sub.add_parser("mine", help="regenerate mined qrels from vault saved queries")
    p_mine.add_argument("--qrels", default=_DEFAULT_QRELS)
    p_mine.set_defaults(func=cmd_mine)

    p_run = sub.add_parser("run", help="score the current config.yml against the qrels")
    p_run.add_argument("--qrels", default=_DEFAULT_QRELS)
    p_run.add_argument("--label", default="run", help="name embedded in the report filename")
    p_run.add_argument("--mode", choices=["hybrid", "bm25", "semantic"], default="hybrid")
    p_run.add_argument("--no-rerank", dest="rerank", action="store_false")
    p_run.add_argument(
        "--expand-graph",
        dest="expand_graph",
        action=argparse.BooleanOptionalAction,
        default=None,
        help="override config graph_expansion.enabled (default: follow config)",
    )
    p_run.add_argument("--k", type=int, default=40, help="chunks requested per query")
    p_run.add_argument("--holdout", action="store_true", help="also score the holdout split (appends to the peek log)")
    p_run.set_defaults(func=cmd_run)

    p_cmp = sub.add_parser("compare", help="diff two run reports")
    p_cmp.add_argument("report_a")
    p_cmp.add_argument("report_b")
    p_cmp.set_defaults(func=cmd_compare)

    args = parser.parse_args(argv)
    raise SystemExit(args.func(args))


if __name__ == "__main__":
    main()
